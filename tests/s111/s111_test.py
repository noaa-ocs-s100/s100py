def test_create_s111(input_data):
    data_file = s111.utils.create_s111("test_s111.h5")

    s111.utils.add_metadata(input_data.metadata, data_file)
    s111.utils.add_data_from_arrays(input_data.speed_01, input_data.direction_01, data_file, input_data.grid_properties, input_data.datetime_value, input_data.data_coding_format)
    s111.utils.add_data_from_arrays(input_data.speed_02, input_data.direction_02, data_file, input_data.grid_properties, input_data.datetime_value, input_data.data_coding_format)
    s111.utils.update_metadata(data_file, input_data.grid_properties, input_data.update_meta)

    s111.utils.write_data_file(data_file)

    assert os.path.isfile("test_s111.h5")
    h5_file = h5py.File("test_s111.h5", "r")

    assert 'Group_F/SurfaceCurrent' in h5_file
    assert 'Group_F/featureCode' in h5_file
    assert 'SurfaceCurrent/SurfaceCurrent.01/uncertainty' in h5_file
    assert 'SurfaceCurrent/axisNames' in h5_file
    assert h5_file['Group_F/SurfaceCurrent'].attrs['chunking'] == input_data.expected_chunks
    assert numpy.allclose(h5_file['SurfaceCurrent/SurfaceCurrent.01/Group_001/values']['surfaceCurrentSpeed'], input_data.speed_01)
    assert numpy.allclose(h5_file['SurfaceCurrent/SurfaceCurrent.01/Group_001/values']['surfaceCurrentDirection'], input_data.direction_01)
    assert numpy.allclose(h5_file['SurfaceCurrent/SurfaceCurrent.01/Group_002/values']['surfaceCurrentSpeed'], input_data.speed_02)
    assert numpy.allclose(h5_file['SurfaceCurrent/SurfaceCurrent.01/Group_002/values']['surfaceCurrentDirection'], input_data.direction_02)
    assert h5_file['SurfaceCurrent/SurfaceCurrent.01/'].attrs['numPointsLongitudinal'] == input_data.speed_01.shape[0]
    assert h5_file['SurfaceCurrent/SurfaceCurrent.01/'].attrs['numPointsLatitudinal'] == input_data.speed_01.shape[1]
    assert all([actual == expected for actual, expected in zip(h5_file['Group_F/SurfaceCurrent'][()], input_data.expected_groupf)])

