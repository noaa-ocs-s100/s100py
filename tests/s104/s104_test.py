def test_create_s104_dcf2(input_data):
    data_file = wtl.utils.create_s104(path_to_s104file)
    water_level_trend_threshold = 0.2

    if wtl.EDITION == 1.0:
        wtl.utils.add_metadata(input_data.metadata_1_0, data_file)
        water_level_trend_threshold = input_data.metadata_1_1['waterLevelTrendThreshold']
    elif wtl.EDITION == 1.1:
        wtl.utils.add_metadata(input_data.metadata_1_1, data_file)
        water_level_trend_threshold = input_data.metadata_1_1['waterLevelTrendThreshold']

    data_series_time_001 = input_data.datetime_forecast_issuance + input_data.datetime_interval
    wtl.utils.add_data_from_arrays(input_data.height_001, input_data.trend_001, data_file, input_data.grid_properties,
                                   data_series_time_001, input_data.data_coding_format)

    trend = numpy.round((input_data.height_002 - input_data.height_001), decimals=2)

    trend_002 = numpy.where((-1 * water_level_trend_threshold < trend) &
                            (trend < water_level_trend_threshold), 3,
                            numpy.where(trend >= water_level_trend_threshold, 2,
                            numpy.where(trend <= -1 * water_level_trend_threshold, 1, numpy.any(trend))))

    data_series_time_002 = data_series_time_001 + input_data.datetime_interval
    wtl.utils.add_data_from_arrays(input_data.height_002, trend_002, data_file, input_data.grid_properties,
                                   data_series_time_002, input_data.data_coding_format)
    wtl.utils.update_metadata(data_file, input_data.grid_properties, input_data.update_meta)

    wtl.utils.write_data_file(data_file)

    assert os.path.isfile(path_to_s104file)
    h5_file = h5py.File(path_to_s104file, "r")

    assert 'Group_F/WaterLevel' in h5_file
    assert 'Group_F/featureCode' in h5_file
    assert 'WaterLevel/WaterLevel.01/uncertainty' in h5_file
    assert 'WaterLevel/axisNames' in h5_file
    if wtl.EDITION == 1.0:
        assert h5_file['Group_F/WaterLevel'].attrs['chunking'] == input_data.expected_chunks
    assert numpy.allclose(h5_file['WaterLevel/WaterLevel.01/Group_001/values']['waterLevelHeight'], input_data.height_001)
    assert numpy.allclose(h5_file['WaterLevel/WaterLevel.01/Group_001/values']['waterLevelTrend'], input_data.trend_001)
    assert numpy.allclose(h5_file['WaterLevel/WaterLevel.01/Group_002/values']['waterLevelHeight'], input_data.height_002)
    assert numpy.allclose(h5_file['WaterLevel/WaterLevel.01/Group_002/values']['waterLevelTrend'], trend_002)
    assert h5_file['WaterLevel/WaterLevel.01/'].attrs['numPointsLongitudinal'] == input_data.height_001.shape[0]
    assert h5_file['WaterLevel/WaterLevel.01/'].attrs['numPointsLatitudinal'] == input_data.height_001.shape[1]

    assert all([h5py_string_comp(actual, expected) for actual, expected in zip(h5_file['Group_F/WaterLevel'][()][0],
                                                                               input_data.expected_groupf[0])])
    assert all([h5py_string_comp(actual, expected) for actual, expected in zip(h5_file['Group_F/WaterLevel'][()][1],
                                                                               input_data.expected_groupf[1])])
    assert all([h5py_string_comp(actual, expected) for actual, expected in zip(h5_file['Group_F/WaterLevel'][()][2],
                                                                               input_data.expected_groupf[2])])


def test_to_geotiff(input_data):
    wtl.utils.to_geotiff(f"{current_directory}/test_s104.h5", current_directory)

    assert os.path.isfile(f"{current_directory}/test_s104_20210901T010000Z.tif")
    assert os.path.isfile(f"{current_directory}/test_s104_20210901T020000Z.tif")

    tif_file1 = gdal.Open(f"{current_directory}/test_s104_20210901T010000Z.tif")
    tif_file2 = gdal.Open(f"{current_directory}/test_s104_20210901T020000Z.tif")

    h5_file = h5py.File(f"{current_directory}/test_s104.h5", "r")
